#include <stdio.h>
#include <locale.h>

int main() {
    setlocale(LC_CTYPE, "");

    // Входные данные
    float m = 5.0;    // время в минутах
    float t = 10.0;   // количество столбов
    float S = 50.0;   // расстояние между столбами в метрах

    // Расчет общего расстояния
    // Между t столбами будет (t-1) промежутков
    float total_distance = (t - 1) * S;  // общее расстояние в метрах

    // Перевод времени из минут в секунды (для скорости в м/с)
    float time_in_seconds = m * 60;

    // Расчет скорости в м/с
    float speed_ms = total_distance / time_in_seconds;

    // Перевод скорости в км/ч
    float speed_kmh = speed_ms * 3.6;

    // Вывод Форматированных результатов
    printf("РАСЧЕТ СКОРОСТИ ПОЕЗДА\n");
    printf("========================\n");
    printf("Входные данные:\n");
    printf("- Время наблюдения: %.2f минут\n", m);
    printf("- Количество пройденных столбов: %.2f\n", t);
    printf("- Расстояние между столбами: %.2f метров\n", S);
    printf("\n");

    printf("Расчет:\n");
    printf("- Количество промежутков между столбами: %.0f\n", t - 1);
    printf("- Общее пройденное расстояние: (%.0f - 1) * %.2f = %.2f метров\n", t, S, total_distance);
    printf("- Время в секундах: %.2f * 60 = %.2f секунд\n", m, time_in_seconds);
    printf("\n");

    printf("Результаты:\n");
    printf("- Скорость поезда: %.2f м/с\n", speed_ms);
    printf("- Скорость поезда: %.2f км/ч\n", speed_kmh);

    return 0;
}